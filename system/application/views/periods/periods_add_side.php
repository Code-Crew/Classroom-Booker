<dl>
  <dt>Time format</dt>
  <dd>The supported formats for the Start time and End time fields are <span>HHMM</span> or <span>HH:MM</span>. For example, <em>0930</em> or <em>14:20</em>. You must also include the leading 0 if using the format HHMM.</dd>
	<dd>All times should also be entered in the <span>24 hour</span> format and be between 00:00 and 23:59.</dd>
</dl>
